package pe.com.acme.inavi.business.bean;

public class InfoNumeroSerieDocumentoBean {
	
	private String serie;
	private Integer numeroDoc;
	private String serieNumeroDoc;

	public String getSerie() {
		return serie;
	}

	public void setSerie(String serie) {
		this.serie = serie;
	}

	public Integer getNumeroDoc() {
		return numeroDoc;
	}

	public void setNumeroDoc(Integer numeroDoc) {
		this.numeroDoc = numeroDoc;
	}

	public String getSerieNumeroDoc() {
		return serieNumeroDoc;
	}

	public void setSerieNumeroDoc(String serieNumeroDoc) {
		this.serieNumeroDoc = serieNumeroDoc;
	}
}
