package pe.com.acme.inavi.business.bean;

public class MetaDataFechaBean {
	
	private int num_anio;
	private int num_mes;
	private int num_dia;
	
	public int getNum_anio() {
		return num_anio;
	}
	public void setNum_anio(int num_anio) {
		this.num_anio = num_anio;
	}
	public int getNum_mes() {
		return num_mes;
	}
	public void setNum_mes(int num_mes) {
		this.num_mes = num_mes;
	}
	public int getNum_dia() {
		return num_dia;
	}
	public void setNum_dia(int num_dia) {
		this.num_dia = num_dia;
	}
	
}
