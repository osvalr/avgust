package pe.com.acme.inoiceelec.util;

public class GeneratedDocInfoDto {
	
	private String numDoc;
	private String numDocElect;
	private String pathFileZip;
	private String message;
	private boolean status;
	
	public String getNumDoc() {
		return numDoc;
	}
	public void setNumDoc(String numDoc) {
		this.numDoc = numDoc;
	}
	public String getPathFileZip() {
		return pathFileZip;
	}
	public void setPathFileZip(String pathFileZip) {
		this.pathFileZip = pathFileZip;
	}
	public boolean isStatus() {
		return status;
	}
	public void setStatus(boolean status) {
		this.status = status;
	}
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getNumDocElect() {
		return numDocElect;
	}
	public void setNumDocElect(String numDocElect) {
		this.numDocElect = numDocElect;
	}

	
}
