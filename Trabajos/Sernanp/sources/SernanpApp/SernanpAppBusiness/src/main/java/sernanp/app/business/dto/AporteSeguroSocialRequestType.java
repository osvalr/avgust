package sernanp.app.business.dto;

public class AporteSeguroSocialRequestType {
	
	private Double uit;
	private Double porcentajeDescuento;
	private Double rmv;
	private Double sueldo;
	private Double porcentajeUit;
	
	public Double getUit() {
		return uit;
	}
	public void setUit(Double uit) {
		this.uit = uit;
	}
	public Double getPorcentajeDescuento() {
		return porcentajeDescuento;
	}
	public void setPorcentajeDescuento(Double porcentajeDescuento) {
		this.porcentajeDescuento = porcentajeDescuento;
	}
	public Double getRmv() {
		return rmv;
	}
	public void setRmv(Double rmv) {
		this.rmv = rmv;
	}
	public Double getSueldo() {
		return sueldo;
	}
	public void setSueldo(Double sueldo) {
		this.sueldo = sueldo;
	}
	public Double getPorcentajeUit() {
		return porcentajeUit;
	}
	public void setPorcentajeUit(Double porcentajeUit) {
		this.porcentajeUit = porcentajeUit;
	}
	
}
